
public class Br6_For_und_IF {

	public static void main(String[] args) {
		
		for (int i = 0; i<10;i++) {
			
			if(i>5) {
			System.out.println("Ich bin größer als 5");
			
			}else {
			System.out.println("Ich bin kleiner als 5");
			}
		}
		
		System.out.println("Schleife beendet");

	}

}
